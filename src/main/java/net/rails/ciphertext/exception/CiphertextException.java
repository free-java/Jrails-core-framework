package net.rails.ciphertext.exception;

public class CiphertextException extends Exception {

	public CiphertextException() {
		super();
	}

	public CiphertextException(String message) {
		super(message);
	}

	public CiphertextException(Throwable cause) {
		super(cause);
	}

	public CiphertextException(String message, Throwable cause) {
		super(message, cause);
	}

}
